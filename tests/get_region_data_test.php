    /**
     * Test that the IPv6 address range checking works correctly
     */
    public function testIpv6AddressRanges() {

        $cidr_prefix = 'fe80::/10';
        $range = ezxISO3166::convertIpv6PrefixToRange($cidr_prefix);

        $this->assertEquals('fe80::', $range[0], "Address range '$cidr_prefix'");
        $this->assertEquals('febf:ffff:ffff:ffff:ffff:ffff:ffff:ffff', $range[1], "Address range '$cidr_prefix'");
    }

    /**
     * Test that an IPv6 address is in a range of addresses
     */
    public function testIpv6AddressWithinRange() {

        $cidr_prefix = 'fe80::/10';
        $range = ezxISO3166::convertIpv6PrefixToRange($cidr_prefix);

        $ip = 'fe90::';
        $in_range = ezxISO3166::isIpV6WithinRange($ip, $range[0], $range[1]);
        $this->assertTrue($in_range, "Address '$ip' is in range '$cidr_prefix'");

        $ip = 'ff00::';
        $in_range = ezxISO3166::isIpV6WithinRange($ip, $range[0], $range[1]);
        $this->assertTrue(!$in_range, "Address '$ip' is not in range '$cidr_prefix'");

        $ip = '1a00::';
        $in_range = ezxISO3166::isIpV6WithinRange($ip, $range[0], $range[1]);
        $this->assertTrue(!$in_range, "Address '$ip' is not in range '$cidr_prefix'");
    }


